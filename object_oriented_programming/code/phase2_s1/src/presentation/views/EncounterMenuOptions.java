package presentation.views;

public enum EncounterMenuOptions {
    /**
     * Option that lets the user add a monster to an encounter
     */
    ADD_MONSTER,

    /**
     * Option that lets the user remove a monster from an encounter
     */
    REMOVE_MONSTER,

    /**
     * Option that lets the user to continue with the next adventure creation step
     */
    CONTINUE
}
