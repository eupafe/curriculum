package presentation.views;

public enum MainMenuOptions {
    /**
     * Option that lets the user create a character in the system
     */
    CHARACTER_CREATION,

    /**
     * Option that lets the user list characters in the system
     */
    LIST_CHARACTERS,

    /**
     * Option that lets the user create an adventure
     */
    CREATE_ADVENTURE,

    /**
     * Option that lets the user start an adventure
     */
    START_ADVENTURE,

    /**
     * Option that lets the user exit the program
     */
    EXIT
}
